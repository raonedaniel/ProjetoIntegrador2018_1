    private void jButtonBUSCARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBUSCARActionPerformed
        // TODO add your handling code here:
        try {
            String consulta = jTextFieldPESQUISAR.getText().toUpperCase();
            DefaultTableModel model = (DefaultTableModel) jTableLISTAVEICULO.getModel();
            model.setNumRows(0);

            if (jRadioButtonNOME.isSelected()) {

                new NVeiculo().consultaPorFiltro(true, consulta).forEachRemaining(listVeiculo -> {
                    model.addRow(new Object[]{
                        listVeiculo.getCodigo(),
                        listVeiculo.getNome(),
                        listVeiculo.getModelo(),
                        listVeiculo.getCor(),
                        listVeiculo.getPlaca(),
                        listVeiculo.getTipoVeiculo().getDescricao()
                    });
                });
                
            } else if (jRadioButtonPLACA.isSelected()) {

                new NVeiculo().consultaPorFiltro(false, consulta).forEachRemaining(listVeiculo -> {
                    model.addRow(new Object[]{
                        listVeiculo.getCodigo(),
                        listVeiculo.getNome(),
                        listVeiculo.getModelo(),
                        listVeiculo.getCor(),
                        listVeiculo.getPlaca(),
                        listVeiculo.getTipoVeiculo().getDescricao()
                    });
                });
                
            } else if (jRadioButtonTIPO.isSelected()) {
                TipoAbstrato
                        .getInstance(jTextFieldPESQUISAR.getText())
                        .getListaDeVeiculos().forEach(listVeiculo -> {
                   model.addRow(new Object[]{
                        listVeiculo.getCodigo(),
                        listVeiculo.getNome(),
                        listVeiculo.getModelo(),
                        listVeiculo.getCor(),
                        listVeiculo.getPlaca(),
                        listVeiculo.getTipoVeiculo().getDescricao()
                   });
               });
            } else {
                listarVeiculo();
            }

        } catch (SQLException ex) {
            //ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro na TelaVeiculo, botão buscar = " + ex.getMessage());
        }

    }//GEN-LAST:event_jButtonBUSCARActionPerformed

