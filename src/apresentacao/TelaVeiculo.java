    private void listarVeiculo() {
        try {

            DefaultTableModel model = (DefaultTableModel) jTableLISTAVEICULO.getModel();
            model.setNumRows(0);
            model.setColumnCount(0);
            model.setColumnIdentifiers(new Object[]{
                "Código",
                "Nome",
                "Modelo",
                "Cor",
                "Placa",
                "Tipo de Veículo"
            });

            new NVeiculo().listar().forEachRemaining(listVeiculo -> {
                model.addRow(new Object[]{
                    listVeiculo.getCodigo(),
                    listVeiculo.getNome(),
                    listVeiculo.getModelo(),
                    listVeiculo.getCor(),
                    listVeiculo.getPlaca(),
                    listVeiculo.getTipoVeiculo().getDescricao()
                });
            });

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro na telaVeiculo, método listarVeiculo = " + e.getMessage());
        }
    }

    private void listarTipoVeiculo() {
        try {
            jComboBoxTIPOVEICULO.removeAllItems();
            for (String descricao : TipoAbstrato.getListaDeTipos()) {
                jComboBoxTIPOVEICULO.addItem(descricao);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro na telaVeiculo, método listarTipoVeiculo = " + e.getMessage());
        }
    }

