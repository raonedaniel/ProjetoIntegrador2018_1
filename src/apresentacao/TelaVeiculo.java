    private void jTableLISTAVEICULOMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableLISTAVEICULOMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTableLISTAVEICULO.getModel();
        jTextFieldCODIGO.setEnabled(false);
        int selecionado = jTableLISTAVEICULO.getSelectedRow();

        if (model.getValueAt(selecionado, 0) != null) {
            jTextFieldCODIGO.setText(model.getValueAt(selecionado, 0).toString());
        }
        if (model.getValueAt(selecionado, 1) != null) {
            jTextFieldNOME.setText(model.getValueAt(selecionado, 1).toString());
        }
        if (model.getValueAt(selecionado, 2) != null) {
            jTextFieldMODELO.setText(model.getValueAt(selecionado, 2).toString());
        }
        if (model.getValueAt(selecionado, 3) != null) {
            jTextFieldCOR.setText(model.getValueAt(selecionado, 3).toString());
        }
        if (model.getValueAt(selecionado, 4) != null) {
            jTextFieldPLACA.setText(model.getValueAt(selecionado, 4).toString());
        }
        if (model.getValueAt(selecionado, 5) != null) {
            jComboBoxTIPOVEICULO.setSelectedItem(model.getValueAt(selecionado, 5).toString());
        }
    }//GEN-LAST:event_jTableLISTAVEICULOMouseClicked

    private void jButtonGRAVARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGRAVARActionPerformed
        // TODO add your handling code here:
        try {
            String nome = jTextFieldNOME.getText();
            String modelo = jTextFieldMODELO.getText();
            String cor = jTextFieldCOR.getText();
            String placa = jTextFieldPLACA.getText();
            String tipoVeiculo = jComboBoxTIPOVEICULO.getSelectedItem().toString();
            int tipoVeiculoN = jComboBoxTIPOVEICULO.getSelectedIndex();

            String valor = jTextFieldCODIGO.getText();
            EVeiculo veiculo = new EVeiculo(nome, modelo, cor, placa,
                    TipoAbstrato.getInstance(tipoVeiculoN + 1));

            if (valor.equals("")) {
                new NVeiculo().salvar(veiculo, valor);
            } else {
                veiculo.setCodigo(Integer.parseInt(jTextFieldCODIGO.getText()));
                new NVeiculo().salvar(veiculo, valor);
            }

            listarVeiculo();

            limparCampos();

        } catch (NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro na TelaVeiculo, botão gravar = " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonGRAVARActionPerformed

